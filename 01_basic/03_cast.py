#03 casting de tipos de datos
#Convertir un entero a flotante
entero = 10
flotante = float(entero)
print("Entero a flotante:")
print(flotante)

#Convertir un flotante a entero
flotante2 = 9.99
entero2 = int(flotante2)
print("Flotante a entero:")
print(entero2)

#Convertir un entero a cadena de texto
entero3 = 42
cadena = str(entero3)
print("Entero a cadena de texto:")
print(cadena)

#Convertir una cadena de texto a entero
cadena2 = "123"
entero4 = int(cadena2)
print("Cadena de texto a entero:")
print(entero4)