#Enteros
print("int:")
print(32)
print(type(32))

#Flotantes
print("float:")
print(3.14)
print(type(3.14))

#complejos
print("complex:")   
print(2 + 3j)
print(type(2 + 3j))

#Booleanos
print("bool:")
print(True)
print(type(True))
print(False)
print(type(False))

#Cadenas de texto
print("str:")
print("Hello, World!")
print(type("Hello, World!"))

#Listas
print("list:")
print([1, 2, 3, 4, 5])
print(type([1, 2, 3, 4, 5]))

#Tuplas
print("tuple:")
print((1, 2, 3, 4, 5))
print(type((1, 2, 3, 4, 5)))

#Conjuntos
print("set:")
print({1, 2, 3, 4, 5})
print(type({1, 2, 3, 4, 5}))

#Diccionarios
print("dict:")
print({"name": "Alice", "age": 30})
print(type({"name": "Alice", "age": 30}))

#NoneType
print("NoneType:")
print(None)
print(type(None))

